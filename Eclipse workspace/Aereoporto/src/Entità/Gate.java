package Entità;


public class Gate {

	private int nGate;
	private String codTratta;

	public Gate(int Ngate, String CodTratta) {
		super();
		nGate = Ngate;
		codTratta = CodTratta;
	}

	public int getNgate() {
		return nGate;
	}

	public void setNgate(int Ngate) {
		nGate = Ngate;
	}

	public String getCodTratta() {
		return codTratta;
	}

	public void setCodTratta(String CodTratta) {
		codTratta = CodTratta;
	}
	
	
	
}
